import { Button, DialogContentText } from '@mui/material';
import { useTranslation } from 'react-i18next';
import { forwardRef, useImperativeHandle, useState } from 'react';
import { DialogProps } from '@mui/material/Dialog';
import { deleteSystemRole, SystemRoleResponse } from '@/api/role';
import CustomizedDialog from '@/components/CustomizedDialog';

interface RoleDeleteProps {
  onSubmit: () => void;
}

const RoleDelete = forwardRef(({ onSubmit }: RoleDeleteProps, ref) => {
  const { t } = useTranslation();

  const [open, setOpen] = useState(false);
  const [maxWidth] = useState<DialogProps['maxWidth']>('sm');

  const [role, setRole] = useState<SystemRoleResponse>();

  useImperativeHandle(ref, () => ({
    show(role: SystemRoleResponse) {
      setRole(role);
      setOpen(true);
    },
    hide() {
      setOpen(false);
    },
  }));

  const handleCancel = () => {
    setOpen(false);
  };

  const handleClose = () => {
    setOpen(false);
  };

  const handleSubmit = async () => {
    if (role) {
      await deleteSystemRole(role.id);
    }
    handleClose();
    onSubmit();
  };

  return (
    <CustomizedDialog
      open={open}
      onClose={handleClose}
      title={t('global.operate.delete') + t('global.page.role')}
      maxWidth={maxWidth}
      actions={
        <>
          <Button onClick={handleSubmit}>{t('global.operate.confirm')}</Button>
          <Button onClick={handleCancel}>{t('global.operate.cancel')}</Button>
        </>
      }
    >
      <DialogContentText>
        {t('global.description.delete', { name: role && role.name })}
      </DialogContentText>
    </CustomizedDialog>
  )
});

export default RoleDelete;